def evaluate_llm(model, tokenizer, prompt_func, dataset, setting_name, num_examples=3):
    print(f"\nEvaluating: {setting_name}")
    all_preds, all_labels = [], []
    start_time = time.time()
    for i, row in tqdm(dataset.iterrows(), total=len(dataset)):
        prompt = prompt_func(row['text'])
        inputs = tokenizer(prompt, return_tensors="pt").to(device)
        with torch.no_grad():
            outputs = model.generate(
                **inputs, max_new_tokens=50, do_sample=False,
                pad_token_id=tokenizer.eos_token_id
            )
        response = tokenizer.decode(
            outputs[0][inputs.input_ids.shape[1]:],
            skip_special_tokens=True
        ).strip()
        pred = parse_emotion(response)
        all_preds.append(pred)
        all_labels.append(row['emotion_label'])
        if i < num_examples:
            print(f"\nExample {i+1}\nPrompt: {prompt}\nOutput: {response}\nParsed: {pred}")
    inf_time = time.time() - start_time
    valid_idx = [i for i, p in enumerate(all_preds) if p != "unknown"]
    v_labels  = [all_labels[i] for i in valid_idx]
    v_preds   = [all_preds[i]  for i in valid_idx]
    acc = accuracy_score(v_labels, v_preds)
    f1  = f1_score(v_labels, v_preds, average='macro', labels=VALID_EMOTIONS)
    print(f"\n{setting_name} - Acc: {acc:.4f}, F1: {f1:.4f}, Time: {inf_time:.2f}s")
    cm = confusion_matrix(v_labels, v_preds, labels=VALID_EMOTIONS)
    plt.figure(figsize=(6, 4))
    sns.heatmap(cm, annot=True, fmt='d',
                xticklabels=VALID_EMOTIONS, yticklabels=VALID_EMOTIONS)
    plt.title(setting_name); plt.tight_layout(); plt.show()
    return acc, f1, inf_time, all_preds

