class EmotionDataset(Dataset):
    def __init__(self, df, word2idx, max_len):
        self.texts = df['text'].tolist()
        self.labels = df['label'].tolist()
        self.word2idx = word2idx; self.max_len = max_len
    def __len__(self): return len(self.labels)
    def __getitem__(self, idx):
        tokens  = simple_tokenize(self.texts[idx])[:self.max_len]
        indices = [self.word2idx.get(t, 1) for t in tokens]
        return torch.tensor(indices, dtype=torch.long), torch.tensor(self.labels[idx], dtype=torch.long)

def rnn_collate_fn(batch):
    texts, labels = zip(*batch)
    lengths = torch.tensor([len(t) for t in texts])
    texts_padded = nn.utils.rnn.pad_sequence(texts, batch_first=True, padding_value=0)
    return texts_padded, torch.stack(labels), lengths

class EmotionRNN(nn.Module):
    def __init__(self, vocab_size, embed_dim, hidden_dim, output_dim, n_layers, dropout, rnn_type='GRU'):
        super().__init__()
        self.embedding = nn.Embedding(vocab_size, embed_dim, padding_idx=0)
        rnn_cls = nn.GRU if rnn_type == 'GRU' else nn.LSTM
        self.rnn = rnn_cls(embed_dim, hidden_dim, num_layers=n_layers,
                           batch_first=True, dropout=dropout, bidirectional=True)
        self.fc = nn.Linear(hidden_dim * 2, output_dim)
        self.dropout = nn.Dropout(dropout)
    def forward(self, text, lengths):
        embedded = self.dropout(self.embedding(text))
        packed = nn.utils.rnn.pack_padded_sequence(embedded, lengths.cpu(),
                     batch_first=True, enforce_sorted=False)
        _, hidden = self.rnn(packed)
        if isinstance(hidden, tuple): hidden = hidden[0]
        hidden = torch.cat((hidden[-2], hidden[-1]), dim=1)
        return self.fc(self.dropout(hidden))

