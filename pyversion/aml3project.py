# ── Dataset class ────────────────────────────────────────────────────────────
# padding=False here — DataCollatorWithPadding will pad each batch dynamically
# to that batch's own max length, exactly like Part 2's rnn_collate_fn.
# This is 2–3x more memory-efficient than padding every sample to max_length=64.
class TransformerDataset(Dataset):
    def __init__(self, df, tokenizer, max_len):
        self.labels = torch.tensor(df['label'].values, dtype=torch.long)
        # Tokenize without padding — collator handles it per-batch
        self.encodings = tokenizer(
            df['text'].tolist(),
            truncation=True,
            padding=False,          # ← key change from original
            max_length=max_len,
        )

    def __len__(self):
        return len(self.labels)

    def __getitem__(self, idx):
        # Return a dict that DataCollatorWithPadding expects
        item = {key: torch.tensor(val[idx])
                for key, val in self.encodings.items()}
        item['labels'] = self.labels[idx]
        return item


# ── Universal training loop ───────────────────────────────────────────────────
def train_transformer(model, train_loader, val_loader, epochs, lr,
                      warmup_ratio, model_name):
    """
    Fine-tunes a HuggingFace sequence-classification model.

    Improvements over original:
      • Linear warmup + linear decay scheduler  — standard for BERT fine-tuning
      • Gradient clipping (max_norm=1.0)        — mirrors Part 2's RNN training
      • Returns val_loss per epoch for plotting
      • Saves best checkpoint by validation loss
    """
    total_steps   = len(train_loader) * epochs
    warmup_steps  = int(total_steps * warmup_ratio)

    optimizer = AdamW(model.parameters(), lr=lr, weight_decay=0.01)

    # Linear warmup then linear decay — the gold standard for transformer fine-tuning
    scheduler = get_linear_schedule_with_warmup(
        optimizer,
        num_warmup_steps=warmup_steps,
        num_training_steps=total_steps
    )

    train_losses, val_losses = [], []
    best_val_loss = float('inf')
    start_time    = time.time()

    for epoch in range(epochs):
        # ── Training phase ──────────────────────────────────────────────────
        model.train()
        epoch_train_loss = 0

        for batch in train_loader:
            input_ids      = batch['input_ids'].to(device)
            attention_mask = batch['attention_mask'].to(device)
            labels         = batch['labels'].to(device)

            optimizer.zero_grad()
            outputs = model(input_ids=input_ids,
                            attention_mask=attention_mask,
                            labels=labels)
            loss = outputs.loss
            loss.backward()

            # Gradient clipping — prevents rare instability spikes
            torch.nn.utils.clip_grad_norm_(model.parameters(), max_norm=1.0)

            optimizer.step()
            scheduler.step()   # step per batch, not per epoch
            epoch_train_loss += loss.item()

        # ── Validation phase ─────────────────────────────────────────────────
        model.eval()
        epoch_val_loss = 0

        with torch.no_grad():
            for batch in val_loader:
                input_ids      = batch['input_ids'].to(device)
                attention_mask = batch['attention_mask'].to(device)
                labels         = batch['labels'].to(device)
                outputs = model(input_ids=input_ids,
                                attention_mask=attention_mask,
                                labels=labels)
                epoch_val_loss += outputs.loss.item()

        avg_train = epoch_train_loss / len(train_loader)
        avg_val   = epoch_val_loss   / len(val_loader)
        train_losses.append(avg_train)
        val_losses.append(avg_val)
        current_lr = scheduler.get_last_lr()[0]

        if avg_val < best_val_loss:
            best_val_loss = avg_val
            torch.save(model.state_dict(), model_name)
            print(f"  Epoch {epoch+1:02} | Train: {avg_train:.4f} | "
                  f"Val: {avg_val:.4f} | LR: {current_lr:.2e} ✅")
        else:
            print(f"  Epoch {epoch+1:02} | Train: {avg_train:.4f} | "
                  f"Val: {avg_val:.4f} | LR: {current_lr:.2e}")

    elapsed = time.time() - start_time
    print(f"\nTotal Training Time: {elapsed:.2f}s ({elapsed/60:.2f} min)")
    return train_losses, val_losses, elapsed

